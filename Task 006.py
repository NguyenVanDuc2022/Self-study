"""
Write a method that calculates the square of a number.

Suggestions:
Utilize the ** operator.
--- Nguyen Van Duc ---
"""
x = int(input("Enter a number: "))


def square(num):
    return num ** 2


print(square(2))  # print square of 2
print(square(3))  # print square of 3
print(square(x))  # print square of x
